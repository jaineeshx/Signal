/**
 * Get a translated string for the current language.
 * @param {string} key - Translation key
 * @param {string} [lang] - Override language (defaults to app state)
 * @returns {string}
 */
function t(key, lang) {
  const l = lang || (window.SIGNAL_STATE?.language) || 'en';
  return (I18N[l] && I18N[l][key]) || I18N['en'][key] || key;
}

window.t = t;
window.I18N = I18N;
